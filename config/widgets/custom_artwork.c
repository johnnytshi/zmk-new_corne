// Create LVGL image descriptor
static const lv_img_dsc_t img_dsc = {
    .header.cf = LV_IMG_CF_INDEXED_1BIT,  // 1-bit per pixel
    .header.always_zero = 0,
    .header.reserved = 0,
    .header.w = 140,  // Updated width to 140
    .header.h = 68,   // Updated height to 68
    .data_size = sizeof(image_data),
    .data = image_data,
};

int zmk_widget_artwork_init(struct zmk_widget_artwork *widget, lv_obj_t *parent) {
    widget->obj = lv_img_create(parent);
    lv_img_set_src(widget->obj, &img_dsc);
    
    // Position the image properly on the OLED display
    lv_obj_set_size(widget->obj, 140, 68);
    
    // Center vertically
    lv_obj_set_pos(widget->obj, 0, (32 - 32) / 2);
    
    // Scale down to fit on the display width
    float scale_factor = 128.0f / 140.0f;
    lv_obj_set_style_transform_zoom(widget->obj, (int)(scale_factor * 256), LV_PART_MAIN);
    
    return 0;
}

int zmk_widget_artwork_draw(struct zmk_widget_artwork *widget) {
    // Nothing to do for static image
    return 0;
}

